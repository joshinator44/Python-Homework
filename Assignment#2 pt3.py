# Basic arithmetic operators

print(7 + 3)
print(7 - 3)
print(7 * 3)
print(7 / 3)

# Two operands

print(7 // 3) # Truncates decimal
print(7 ** 3) # Raises to the power of

# Modulo operator
print(7 % 3) # Returns the remainder of

# Order of precedence (Default)
# MUltiplication and division will happen first
print( 7 + 3 * 5)

# User specified order
